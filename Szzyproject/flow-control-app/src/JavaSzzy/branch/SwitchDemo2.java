package JavaSzzy.branch;

public class SwitchDemo2 {
    public static void main(String[] args) {
        //Master the use of Switch statement and understand its execution flow
        String week="0";
        switch (week){
            case "1":
                System.out.println("Put your head down and code");
                break;
            case"2":
                System.out.println("Ask help from a seasoned Java developer");
                break;
                default:
                    System.out.println("The information you entered is incorrect");
        }
    }
}
