package JavaSzzy.variable;

public class ACSIIDemo1 {
    public static void main(String[] args) {
        System.out.println('a'+10);
        // binary octonary hexadecimal
        int a1=0B01100001;//
        System.out.println("a1");
        System.out.println(a1);

        int a2=0141;//octonary
        System.out.println(a2);

        int a3=0XFA;//hexadecimal
        System.out.println(a3);
    }

}
